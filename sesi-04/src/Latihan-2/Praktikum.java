//class ini untuk menampilkan nama pada filenya.
public class Praktikum{
	//method utama yang akan menjalankan oleh java.
	public static void main(String args[]){
		//dideklarasikan variabel x dengan tipe data for (integer x) akan menghitung variabel dari 0-6
		for (int x = 0; x < 7; x++){
			//menjalankan perintah dengan variabel perbandingan x < 7 apabila x lebih kecil dari 7 maka perintah dijalankan.
			System.out.println("Hello, Worlds we are using loops");
		}
	}
}